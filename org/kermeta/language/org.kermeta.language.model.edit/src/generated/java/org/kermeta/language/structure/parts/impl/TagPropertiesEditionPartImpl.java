		SWTUtils.createPartLabel(parent, StructureMessages.TagPropertiesEditionPart_KTypeLabel, propertiesEditionComponent.isRequired(StructureViewsRepository.Tag.Properties.kType, StructureViewsRepository.SWT_KIND));
