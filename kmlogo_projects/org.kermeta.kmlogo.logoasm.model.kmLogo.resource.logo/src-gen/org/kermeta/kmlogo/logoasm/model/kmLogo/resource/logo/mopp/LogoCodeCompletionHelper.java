package org.kermeta.kmlogo.logoasm.model.kmLogo.resource.logo.mopp;

// A CodeCompletionHelper can be used to derive completion proposals for partial
// documents. It runs the parser generated by EMFText in a special mode (i.e., the
// rememberExpectedElements mode). Based on the elements that are expected by the
// parser for different regions in the document, valid proposals are computed.
public class LogoCodeCompletionHelper {
	
	// Computes a set of proposals for the given document assuming the cursor is
	// at 'cursorOffset'. The proposals are derived using the meta information, i.e.,
	// the generated language plug-in.
	//
	// @param metaInformation
	// @param content the documents content
	// @param cursorOffset
	// @return
	public org.kermeta.kmlogo.logoasm.model.kmLogo.resource.logo.mopp.LogoCompletionProposal[] computeCompletionProposals(org.kermeta.kmlogo.logoasm.model.kmLogo.resource.logo.ILogoTextResource originalResource, String content, int cursorOffset) {
		org.eclipse.emf.ecore.resource.ResourceSet resourceSet = new org.eclipse.emf.ecore.resource.impl.ResourceSetImpl();
		// the shadow resource needs the same URI because reference resolvers may use the URI to resolve external references
		org.kermeta.kmlogo.logoasm.model.kmLogo.resource.logo.ILogoTextResource resource = (org.kermeta.kmlogo.logoasm.model.kmLogo.resource.logo.ILogoTextResource) resourceSet.createResource(originalResource.getURI());
		java.io.ByteArrayInputStream inputStream = new java.io.ByteArrayInputStream(content.getBytes());
		org.kermeta.kmlogo.logoasm.model.kmLogo.resource.logo.mopp.LogoMetaInformation metaInformation = resource.getMetaInformation();
		org.kermeta.kmlogo.logoasm.model.kmLogo.resource.logo.ILogoTextParser parser = metaInformation.createParser(inputStream, null);
		org.kermeta.kmlogo.logoasm.model.kmLogo.resource.logo.mopp.LogoExpectedTerminal[] expectedElements = parseToExpectedElements(parser, resource);
		if (expectedElements == null) {
			return new org.kermeta.kmlogo.logoasm.model.kmLogo.resource.logo.mopp.LogoCompletionProposal[0];
		}
		if (expectedElements.length == 0) {
			return new org.kermeta.kmlogo.logoasm.model.kmLogo.resource.logo.mopp.LogoCompletionProposal[0];
		}
		java.util.List<org.kermeta.kmlogo.logoasm.model.kmLogo.resource.logo.mopp.LogoExpectedTerminal> expectedAfterCursor = java.util.Arrays.asList(getElementsExpectedAt(expectedElements, cursorOffset));
		java.util.List<org.kermeta.kmlogo.logoasm.model.kmLogo.resource.logo.mopp.LogoExpectedTerminal> expectedBeforeCursor = java.util.Arrays.asList(getElementsExpectedAt(expectedElements, cursorOffset - 1));
		setPrefixes(expectedAfterCursor, content, cursorOffset);
		setPrefixes(expectedBeforeCursor, content, cursorOffset);
		// first we derive all possible proposals from the set of elements that are expected at the cursor position
		java.util.Collection<org.kermeta.kmlogo.logoasm.model.kmLogo.resource.logo.mopp.LogoCompletionProposal> allProposals = new java.util.LinkedHashSet<org.kermeta.kmlogo.logoasm.model.kmLogo.resource.logo.mopp.LogoCompletionProposal>();
		java.util.Collection<org.kermeta.kmlogo.logoasm.model.kmLogo.resource.logo.mopp.LogoCompletionProposal> rightProposals = deriveProposals(expectedAfterCursor, content, resource, cursorOffset);
		java.util.Collection<org.kermeta.kmlogo.logoasm.model.kmLogo.resource.logo.mopp.LogoCompletionProposal> leftProposals = deriveProposals(expectedBeforeCursor, content, resource, cursorOffset);
		// second, the set of left proposals (i.e., the ones before the cursor) is
		// checked for emptiness. if the set is empty, the right proposals (i.e., 
		// the ones after the cursor are removed, because it does not make sense to
		// propose them until the element before the cursor was completed
		allProposals.addAll(leftProposals);
		if (leftProposals.isEmpty()) {
			allProposals.addAll(rightProposals);
		}
		// third, the proposals are sorted according to their relevance
		// proposals that matched the prefix are preferred over ones that did not
		// afterward proposals are sorted alphabetically
		final java.util.List<org.kermeta.kmlogo.logoasm.model.kmLogo.resource.logo.mopp.LogoCompletionProposal> sortedProposals = new java.util.ArrayList<org.kermeta.kmlogo.logoasm.model.kmLogo.resource.logo.mopp.LogoCompletionProposal>(allProposals);
		java.util.Collections.sort(sortedProposals);
		return sortedProposals.toArray(new org.kermeta.kmlogo.logoasm.model.kmLogo.resource.logo.mopp.LogoCompletionProposal[sortedProposals.size()]);
	}
	
	public org.kermeta.kmlogo.logoasm.model.kmLogo.resource.logo.mopp.LogoExpectedTerminal[] parseToExpectedElements(org.kermeta.kmlogo.logoasm.model.kmLogo.resource.logo.ILogoTextParser parser, org.kermeta.kmlogo.logoasm.model.kmLogo.resource.logo.ILogoTextResource resource) {
		final java.util.List<org.kermeta.kmlogo.logoasm.model.kmLogo.resource.logo.mopp.LogoExpectedTerminal> expectedElements = parser.parseToExpectedElements(null, resource);
		if (expectedElements == null) {
			return new org.kermeta.kmlogo.logoasm.model.kmLogo.resource.logo.mopp.LogoExpectedTerminal[0];
		}
		removeDuplicateEntries(expectedElements);
		removeInvalidEntriesAtEnd(expectedElements);
		return expectedElements.toArray(new org.kermeta.kmlogo.logoasm.model.kmLogo.resource.logo.mopp.LogoExpectedTerminal[expectedElements.size()]);
	}
	
	private void removeDuplicateEntries(java.util.List<org.kermeta.kmlogo.logoasm.model.kmLogo.resource.logo.mopp.LogoExpectedTerminal> expectedElements) {
		for (int i = 0; i < expectedElements.size() - 1; i++) {
			org.kermeta.kmlogo.logoasm.model.kmLogo.resource.logo.mopp.LogoExpectedTerminal elementAtIndex = expectedElements.get(i);
			for (int j = i + 1; j < expectedElements.size();) {
				org.kermeta.kmlogo.logoasm.model.kmLogo.resource.logo.mopp.LogoExpectedTerminal elementAtNext = expectedElements.get(j);
				if (elementAtIndex.equals(elementAtNext) && elementAtIndex.getStartExcludingHiddenTokens() == elementAtNext.getStartExcludingHiddenTokens()) {
					expectedElements.remove(j);
				} else {
					j++;
				}
			}
		}
	}
	
	private void removeInvalidEntriesAtEnd(java.util.List<org.kermeta.kmlogo.logoasm.model.kmLogo.resource.logo.mopp.LogoExpectedTerminal> expectedElements) {
		for (int i = 0; i < expectedElements.size() - 1;) {
			org.kermeta.kmlogo.logoasm.model.kmLogo.resource.logo.mopp.LogoExpectedTerminal elementAtIndex = expectedElements.get(i);
			org.kermeta.kmlogo.logoasm.model.kmLogo.resource.logo.mopp.LogoExpectedTerminal elementAtNext = expectedElements.get(i + 1);
			if (elementAtIndex.getStartExcludingHiddenTokens() == elementAtNext.getStartExcludingHiddenTokens() && shouldRemove(elementAtIndex.getFollowSetID(), elementAtNext.getFollowSetID())) {
				expectedElements.remove(i + 1);
			} else {
				i++;
			}
		}
	}
	
	public boolean shouldRemove(int followSetID1, int followSetID2) {
		return followSetID1 != followSetID2;
	}
	
	private String findPrefix(java.util.List<org.kermeta.kmlogo.logoasm.model.kmLogo.resource.logo.mopp.LogoExpectedTerminal> expectedElements, org.kermeta.kmlogo.logoasm.model.kmLogo.resource.logo.mopp.LogoExpectedTerminal expectedAtCursor, String content, int cursorOffset) {
		if (cursorOffset < 0) {
			return "";
		}
		int end = 0;
		for (org.kermeta.kmlogo.logoasm.model.kmLogo.resource.logo.mopp.LogoExpectedTerminal expectedElement : expectedElements) {
			if (expectedElement == expectedAtCursor) {
				final int start = expectedElement.getStartExcludingHiddenTokens();
				if (start >= 0  && start < Integer.MAX_VALUE) {
					end = start;
				}
				break;
			}
		}
		end = Math.min(end, cursorOffset);
		final String prefix = content.substring(end, Math.min(content.length(), cursorOffset));
		return prefix;
	}
	
	private java.util.Collection<org.kermeta.kmlogo.logoasm.model.kmLogo.resource.logo.mopp.LogoCompletionProposal> deriveProposals(java.util.List<org.kermeta.kmlogo.logoasm.model.kmLogo.resource.logo.mopp.LogoExpectedTerminal> expectedElements, String content, org.kermeta.kmlogo.logoasm.model.kmLogo.resource.logo.ILogoTextResource resource, int cursorOffset) {
		java.util.Collection<org.kermeta.kmlogo.logoasm.model.kmLogo.resource.logo.mopp.LogoCompletionProposal> resultSet = new java.util.HashSet<org.kermeta.kmlogo.logoasm.model.kmLogo.resource.logo.mopp.LogoCompletionProposal>();
		for (org.kermeta.kmlogo.logoasm.model.kmLogo.resource.logo.mopp.LogoExpectedTerminal expectedElement : expectedElements) {
			resultSet.addAll(deriveProposals(expectedElement, content, resource, cursorOffset));
		}
		return resultSet;
	}
	
	private java.util.Collection<org.kermeta.kmlogo.logoasm.model.kmLogo.resource.logo.mopp.LogoCompletionProposal> deriveProposals(org.kermeta.kmlogo.logoasm.model.kmLogo.resource.logo.mopp.LogoExpectedTerminal expectedTerminal, String content, org.kermeta.kmlogo.logoasm.model.kmLogo.resource.logo.ILogoTextResource resource, int cursorOffset) {
		org.kermeta.kmlogo.logoasm.model.kmLogo.resource.logo.mopp.LogoMetaInformation metaInformation = resource.getMetaInformation();
		org.kermeta.kmlogo.logoasm.model.kmLogo.resource.logo.ILogoLocationMap locationMap = resource.getLocationMap();
		org.kermeta.kmlogo.logoasm.model.kmLogo.resource.logo.ILogoExpectedElement expectedElement = (org.kermeta.kmlogo.logoasm.model.kmLogo.resource.logo.ILogoExpectedElement) expectedTerminal.getTerminal();
		if (expectedElement instanceof org.kermeta.kmlogo.logoasm.model.kmLogo.resource.logo.mopp.LogoExpectedCsString) {
			org.kermeta.kmlogo.logoasm.model.kmLogo.resource.logo.mopp.LogoExpectedCsString csString = (org.kermeta.kmlogo.logoasm.model.kmLogo.resource.logo.mopp.LogoExpectedCsString) expectedElement;
			return deriveProposal(csString, content, expectedTerminal.getPrefix(), cursorOffset);
		} else if (expectedElement instanceof org.kermeta.kmlogo.logoasm.model.kmLogo.resource.logo.mopp.LogoExpectedStructuralFeature) {
			org.kermeta.kmlogo.logoasm.model.kmLogo.resource.logo.mopp.LogoExpectedStructuralFeature expectedFeature = (org.kermeta.kmlogo.logoasm.model.kmLogo.resource.logo.mopp.LogoExpectedStructuralFeature) expectedElement;
			org.eclipse.emf.ecore.EStructuralFeature feature = expectedFeature.getFeature();
			org.eclipse.emf.ecore.EClassifier featureType = feature.getEType();
			java.util.List<org.eclipse.emf.ecore.EObject> elementsAtCursor = locationMap.getElementsAt(cursorOffset);
			org.eclipse.emf.ecore.EObject container = null;
			// we need to skip the proxy elements at the cursor, because they are not the container for the reference we try to complete
			for (int i = 0; i < elementsAtCursor.size(); i++) {
				container = elementsAtCursor.get(i);
				if (!container.eIsProxy()) {
					break;
				}
			}
			if (feature instanceof org.eclipse.emf.ecore.EReference) {
				org.eclipse.emf.ecore.EReference reference = (org.eclipse.emf.ecore.EReference) feature;
				if (featureType instanceof org.eclipse.emf.ecore.EClass) {
					if (reference.isContainment()) {
						// the FOLLOW set should contain only non-containment references
						assert false;
					} else {
						return handleNCReference(metaInformation, container, reference, expectedTerminal.getPrefix());
					}
				}
			} else if (feature instanceof org.eclipse.emf.ecore.EAttribute) {
				org.eclipse.emf.ecore.EAttribute attribute = (org.eclipse.emf.ecore.EAttribute) feature;
				if (featureType instanceof org.eclipse.emf.ecore.EEnum) {
					org.eclipse.emf.ecore.EEnum enumType = (org.eclipse.emf.ecore.EEnum) featureType;
					return deriveProposals(expectedTerminal, enumType, content, cursorOffset);
				} else {
					// handle EAttributes (derive default value depending on
					// the type of the attribute, figure out token resolver, and
					// call deResolve())
					return handleAttribute(metaInformation, expectedFeature, container, attribute, expectedTerminal.getPrefix());
				}
			} else {
				// there should be no other subclass of EStructuralFeature
				assert false;
			}
		} else {
			// there should be no other class implementing IExpectedElement
			assert false;
		}
		return java.util.Collections.emptyList();
	}
	
	private java.util.Collection<org.kermeta.kmlogo.logoasm.model.kmLogo.resource.logo.mopp.LogoCompletionProposal> deriveProposals(org.kermeta.kmlogo.logoasm.model.kmLogo.resource.logo.mopp.LogoExpectedTerminal expectedElement, org.eclipse.emf.ecore.EEnum enumType, String content, int cursorOffset) {
		java.util.Collection<org.eclipse.emf.ecore.EEnumLiteral> enumLiterals = enumType.getELiterals();
		java.util.Collection<org.kermeta.kmlogo.logoasm.model.kmLogo.resource.logo.mopp.LogoCompletionProposal> result = new java.util.HashSet<org.kermeta.kmlogo.logoasm.model.kmLogo.resource.logo.mopp.LogoCompletionProposal>();
		for (org.eclipse.emf.ecore.EEnumLiteral literal : enumLiterals) {
			String proposal = literal.getLiteral();
			String prefix = expectedElement.getPrefix();
			if (matches(proposal, prefix)) {
				result.add(new org.kermeta.kmlogo.logoasm.model.kmLogo.resource.logo.mopp.LogoCompletionProposal(proposal, prefix, !"".equals(prefix), true));
			}
		}
		return result;
	}
	
	private java.util.Collection<org.kermeta.kmlogo.logoasm.model.kmLogo.resource.logo.mopp.LogoCompletionProposal> handleNCReference(org.kermeta.kmlogo.logoasm.model.kmLogo.resource.logo.mopp.LogoMetaInformation metaInformation, org.eclipse.emf.ecore.EObject container, org.eclipse.emf.ecore.EReference reference, java.lang.String prefix) {
		// proposals for non-containment references are derived by calling the
		// reference resolver switch in fuzzy mode.
		org.kermeta.kmlogo.logoasm.model.kmLogo.resource.logo.ILogoReferenceResolverSwitch resolverSwitch = metaInformation.getReferenceResolverSwitch();
		org.kermeta.kmlogo.logoasm.model.kmLogo.resource.logo.ILogoReferenceResolveResult<org.eclipse.emf.ecore.EObject> result = new org.kermeta.kmlogo.logoasm.model.kmLogo.resource.logo.mopp.LogoReferenceResolveResult<org.eclipse.emf.ecore.EObject>(true);
		resolverSwitch.resolveFuzzy(prefix, container, reference, 0, result);
		java.util.Collection<org.kermeta.kmlogo.logoasm.model.kmLogo.resource.logo.ILogoReferenceMapping<org.eclipse.emf.ecore.EObject>> mappings = result.getMappings();
		if (mappings != null) {
			java.util.Collection<org.kermeta.kmlogo.logoasm.model.kmLogo.resource.logo.mopp.LogoCompletionProposal> resultSet = new java.util.HashSet<org.kermeta.kmlogo.logoasm.model.kmLogo.resource.logo.mopp.LogoCompletionProposal>();
			for (org.kermeta.kmlogo.logoasm.model.kmLogo.resource.logo.ILogoReferenceMapping<org.eclipse.emf.ecore.EObject> mapping : mappings) {
				final String identifier = mapping.getIdentifier();
				// the proposal can be added without checking the prefix because this is
				// performed by the reference resolvers
				resultSet.add(new org.kermeta.kmlogo.logoasm.model.kmLogo.resource.logo.mopp.LogoCompletionProposal(identifier, prefix, true, true));
			}
			return resultSet;
		}
		return java.util.Collections.emptyList();
	}
	
	private java.util.Collection<org.kermeta.kmlogo.logoasm.model.kmLogo.resource.logo.mopp.LogoCompletionProposal> handleAttribute(org.kermeta.kmlogo.logoasm.model.kmLogo.resource.logo.mopp.LogoMetaInformation metaInformation, org.kermeta.kmlogo.logoasm.model.kmLogo.resource.logo.mopp.LogoExpectedStructuralFeature expectedFeature, org.eclipse.emf.ecore.EObject container, org.eclipse.emf.ecore.EAttribute attribute, java.lang.String prefix) {
		java.lang.Object defaultValue = getDefaultValue(attribute);
		if (defaultValue != null) {
			org.kermeta.kmlogo.logoasm.model.kmLogo.resource.logo.ILogoTokenResolverFactory tokenResolverFactory = metaInformation.getTokenResolverFactory();
			String tokenName = expectedFeature.getTokenName();
			if (tokenName != null) {
				org.kermeta.kmlogo.logoasm.model.kmLogo.resource.logo.ILogoTokenResolver tokenResolver = tokenResolverFactory.createTokenResolver(tokenName);
				if (tokenResolver != null) {
					String defaultValueAsString = tokenResolver.deResolve(defaultValue, attribute, container);
					java.util.Collection<org.kermeta.kmlogo.logoasm.model.kmLogo.resource.logo.mopp.LogoCompletionProposal> resultSet = new java.util.HashSet<org.kermeta.kmlogo.logoasm.model.kmLogo.resource.logo.mopp.LogoCompletionProposal>();
					if (matches(defaultValueAsString, prefix)) {
						resultSet.add(new org.kermeta.kmlogo.logoasm.model.kmLogo.resource.logo.mopp.LogoCompletionProposal(defaultValueAsString, prefix, !"".equals(prefix), true));
					}
					return resultSet;
				}
			}
		}
		return java.util.Collections.emptyList();
	}
	
	private java.lang.Object getDefaultValue(org.eclipse.emf.ecore.EAttribute attribute) {
		String typeName = attribute.getEType().getName();
		if ("EString".equals(typeName)) {
			return "some" + org.kermeta.kmlogo.logoasm.model.kmLogo.resource.logo.util.LogoStringUtil.capitalize(attribute.getName());
		}
		return attribute.getDefaultValue();
	}
	
	private java.util.Collection<org.kermeta.kmlogo.logoasm.model.kmLogo.resource.logo.mopp.LogoCompletionProposal> deriveProposal(org.kermeta.kmlogo.logoasm.model.kmLogo.resource.logo.mopp.LogoExpectedCsString csString, String content, String prefix, int cursorOffset) {
		String proposal = csString.getValue();
		java.util.Collection<org.kermeta.kmlogo.logoasm.model.kmLogo.resource.logo.mopp.LogoCompletionProposal> result = new java.util.HashSet<org.kermeta.kmlogo.logoasm.model.kmLogo.resource.logo.mopp.LogoCompletionProposal>();
		if (matches(proposal, prefix)) {
			result.add(new org.kermeta.kmlogo.logoasm.model.kmLogo.resource.logo.mopp.LogoCompletionProposal(proposal, prefix, !"".equals(prefix), false));
		}
		return result;
	}
	
	// for each given expected elements the prefix is calculated
	// the prefix depends on the current document content, the cursor position, and
	// the position where the element is expected
	private void setPrefixes(java.util.List<org.kermeta.kmlogo.logoasm.model.kmLogo.resource.logo.mopp.LogoExpectedTerminal> expectedElements, String content, int cursorOffset) {
		if (cursorOffset < 0) {
			return;
		}
		for (org.kermeta.kmlogo.logoasm.model.kmLogo.resource.logo.mopp.LogoExpectedTerminal expectedElement : expectedElements) {
			java.lang.String prefix = findPrefix(expectedElements, expectedElement, content, cursorOffset);
			expectedElement.setPrefix(prefix);
		}
	}
	
	public org.kermeta.kmlogo.logoasm.model.kmLogo.resource.logo.mopp.LogoExpectedTerminal[] getElementsExpectedAt(org.kermeta.kmlogo.logoasm.model.kmLogo.resource.logo.mopp.LogoExpectedTerminal[] allExpectedElements, int cursorOffset) {
		java.util.List<org.kermeta.kmlogo.logoasm.model.kmLogo.resource.logo.mopp.LogoExpectedTerminal> expectedAtCursor = new java.util.ArrayList<org.kermeta.kmlogo.logoasm.model.kmLogo.resource.logo.mopp.LogoExpectedTerminal>();
		for (int i = 0; i < allExpectedElements.length; i++) {
			org.kermeta.kmlogo.logoasm.model.kmLogo.resource.logo.mopp.LogoExpectedTerminal expectedElement = allExpectedElements[i];
			int startIncludingHidden = expectedElement.getStartIncludingHiddenTokens();
			int end = getEnd(allExpectedElements, i);
			if (cursorOffset >= startIncludingHidden && cursorOffset <= end) {
				expectedAtCursor.add(expectedElement);
			}
		}
		return expectedAtCursor.toArray(new org.kermeta.kmlogo.logoasm.model.kmLogo.resource.logo.mopp.LogoExpectedTerminal[expectedAtCursor.size()]);
	}
	
	private int getEnd(org.kermeta.kmlogo.logoasm.model.kmLogo.resource.logo.mopp.LogoExpectedTerminal[] allExpectedElements, int indexInList) {
		org.kermeta.kmlogo.logoasm.model.kmLogo.resource.logo.mopp.LogoExpectedTerminal elementAtIndex = allExpectedElements[indexInList];
		int startIncludingHidden = elementAtIndex.getStartIncludingHiddenTokens();
		int startExcludingHidden = elementAtIndex.getStartExcludingHiddenTokens();
		for (int i = indexInList + 1; i < allExpectedElements.length; i++) {
			org.kermeta.kmlogo.logoasm.model.kmLogo.resource.logo.mopp.LogoExpectedTerminal elementAtI = allExpectedElements[i];
			int startIncludingHiddenForI = elementAtI.getStartIncludingHiddenTokens();
			int startExcludingHiddenForI = elementAtI.getStartExcludingHiddenTokens();
			if (startIncludingHidden != startIncludingHiddenForI || startExcludingHidden != startExcludingHiddenForI) {
				return startIncludingHiddenForI - 1;
			}
		}
		return Integer.MAX_VALUE;
	}
	
	private boolean matches(java.lang.String proposal, java.lang.String prefix) {
		return (proposal.startsWith(prefix) || org.kermeta.kmlogo.logoasm.model.kmLogo.resource.logo.util.LogoStringUtil.matchCamelCase(prefix, proposal) != null) && !proposal.equals(prefix);
	}
	
}
